# Relational operator (result in bool value)
# >, <, >=, <=, ==, !=

result = 10 > 5
print("10 > 5 =",result)

result = 10 < 5
print("10 < 5 =",result)

result = 10 >= 10
print("10 >= 10 =",result)

result = 10 <= 10
print("10 <= 10 =",result)

result = "Aman" == "Aman"
print("Aman == Aman =",result)

result = "Aman" != "Ram"
print("Aman != Ram =",result)

password = "123Acb@"

result = len(password) >= 10
print("password >= 10 =",result)